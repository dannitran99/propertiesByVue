<script>
import { gsap } from 'gsap'
import { ScrollTrigger } from 'gsap/ScrollTrigger'
import { MotionPathPlugin } from 'gsap/MotionPathPlugin'
import { ScrollToPlugin } from 'gsap/ScrollToPlugin'

gsap.registerPlugin(ScrollTrigger, MotionPathPlugin, ScrollToPlugin)
export default {
  name: 'report',
  mounted() {
    gsap.to('.planet-wrap-1', {
      scrollTrigger: {
        trigger: '.report-hero',
        scrub: true,
        start: 'top top',
        end: '+=100%'
      },
      motionPath: {
        path: '#path-planet-1',
        align: '#path-planet-1',
        alignOrigin: [0.2, 0.5]
      },
      opacity: 0.3
    })
    gsap.to('.planet-wrap-3', {
      scrollTrigger: {
        trigger: '.report-hero',
        scrub: true,
        start: 'top top',
        end: '+=100%'
      },
      motionPath: {
        path: '#path-planet-3',
        align: '#path-planet-3',
        alignOrigin: [0.33, 0.5]
      },
      opacity: 0.3
    })
    gsap.to('.planet-wrap-4', {
      scrollTrigger: {
        trigger: '.report-hero',
        scrub: true,
        start: 'top top',
        end: '+=100%'
      },
      motionPath: {
        path: '#path-planet-4',
        align: '#path-planet-4',
        alignOrigin: [0.23, 0.5]
      },
      opacity: 0.3
    })
    gsap.to('.planet-wrap-5', {
      scrollTrigger: {
        trigger: '.report-hero',
        scrub: true,
        start: 'top top',
        end: '+=100%'
      },
      motionPath: {
        path: '#path-planet-5',
        align: '#path-planet-5',
        alignOrigin: [0.17, 0.5]
      },
      opacity: 0.3
    })
    gsap.to('.planet-wrap-2', {
      scrollTrigger: {
        trigger: '.report-hero',
        pin: true,
        scrub: true,
        start: 'top top',
        end: '+=100%'
      },
      motionPath: {
        path: '#path-planet-2',
        align: '#path-planet-2',
        alignOrigin: [0.25, 0.5]
      },
      opacity: 0.3
    })
    const sections = gsap.utils.toArray('.panel-section')
    let currentIndex = -1
    let animating
    gsap.set('.x-100', { xPercent: 100 })
    gsap.set(sections, {
      zIndex: i => i
    })

    // create an observer and disable it to start
    let intentObserver = ScrollTrigger.observe({
      type: 'wheel,touch',
      onUp: () => !animating && gotoPanel(currentIndex + 1, true),
      onDown: () => !animating && gotoPanel(currentIndex - 1, false),
      wheelSpeed: -1, // to match mobile behavior, invert the wheel speed
      tolerance: 10,
      preventDefault: true,
      onPress: self => {
        ScrollTrigger.isTouch && self.event.preventDefault()
      }
    })
    intentObserver.disable()

    let preventScroll = ScrollTrigger.observe({
      preventDefault: true,
      type: 'wheel,scroll',
      allowClicks: true,
      onEnable: self => {
        self.savedScroll = self.scrollY()
      }, // save the scroll position
      onChangeY: self => self.scrollY(self.savedScroll) // refuse to scroll
    })
    preventScroll.disable()

    // handle the panel swipe animations
    function gotoPanel(index, isScrollingDown) {
      if ((isScrollingDown && currentIndex < sections.length - 1) || (!isScrollingDown && currentIndex > 0)) {
        animating = true
        let target = isScrollingDown ? sections[index] : sections[currentIndex]
        gsap.to(target, {
          xPercent: isScrollingDown ? 0 : 100,
          duration: 0.5,
          onStart: () => {
            if (currentIndex === 1) {
              counting(80, sections[1].querySelector('#percentage-1'))
              counting(90, sections[1].querySelector('#percentage-2'))
              counting(54, sections[1].querySelector('#percentage-3'))
              sections[1].querySelector('#progress-animation-1').classList.add('circle-1')
              sections[1].querySelector('#progress-animation-2').classList.add('circle-2')
              sections[1].querySelector('#progress-animation-3').classList.add('circle-3')
            }
          },
          onComplete: () => {
            if (currentIndex !== 1) {
              sections[1].querySelector('#progress-animation-1').classList.remove('circle-1')
              sections[1].querySelector('#progress-animation-2').classList.remove('circle-2')
              sections[1].querySelector('#progress-animation-3').classList.remove('circle-3')
            }
            animating = false
          }
        })
        currentIndex = index
      }
    }

    function counting(count, element) {
      let c = 1
      var timer = setInterval(() => {
        element.innerHTML = c
        c++
        c > count && clearInterval(timer)
      }, 20)
    }

    // pin swipe section and initiate observer
    ScrollTrigger.create({
      trigger: '.scroll',
      pin: true,
      anticipatePin: true,
      start: 'top top',
      end: '+=50%',
      onEnter: (self) => {
        if (preventScroll.isEnabled === false) {
          self.scroll(self.start)
          preventScroll.enable()
          intentObserver.enable()
          gotoPanel(currentIndex + 1, true)
        }
      },
      onEnterBack: (self) => {
        if (preventScroll.isEnabled === false) {
          self.scroll(self.start)
          preventScroll.enable()
          intentObserver.enable()
          gotoPanel(currentIndex - 1, false)
        }
      }
    })
  }
}
